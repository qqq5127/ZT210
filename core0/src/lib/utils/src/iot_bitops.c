/* find the first bit set to 1 for non-zero byte */
static inline uint8_t __iot_bitops_ffs(uint8_t tmp)
{
    uint8_t b = 1;

    if (!(tmp & 0x0F)) {
        tmp >>= 4;
        b += 4;
    }
    if (!(tmp & 0x03)) {
        tmp >>= 2;
        b += 2;
    }
    if (!(tmp & 0x01)) {
        b += 1;
    }

    return b;
}

/* find the last bit set to 1 for non-zero byte */
static inline uint8_t __iot_bitops_fls(uint8_t tmp)
{
    uint8_t b = 8;

    if (!(tmp & 0xF0)) {
        tmp <<= 4;
        b -= 4;
    }
    if (!(tmp & 0xC0)) {
        tmp <<= 2;
        b -= 2;
    }
    if (!(tmp & 0x80)) {
        b -= 1;
    }

    return b;
}

/* find number of bits set to 1 for one byte */
static inline uint8_t __iot_bitops_cbs(uint8_t tmp)
{
    uint8_t b = 0;

    while (tmp) {
        tmp &= tmp - 1;
        b++;
    }

    return b;
}

void iot_bitops_init(void)
{
    uint32_t i;

    for (i = 1; i < IOT_BITOPS_TAB_SIZE; i++) {
        assert(iot_bitops_ffs_tab[i] == __iot_bitops_ffs((uint8_t)i));
    }
    for (i = 1; i < IOT_BITOPS_TAB_SIZE; i++) {
        assert(iot_bitops_fls_tab[i] == __iot_bitops_fls((uint8_t)i));
    }
    for (i = 1; i < IOT_BITOPS_TAB_SIZE; i++) {
        assert(iot_bitops_cbs_tab[i] == __iot_bitops_cbs((uint8_t)i));
    }
}
